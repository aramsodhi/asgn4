int addnums(int a, int b) {
    return a + b;
}

int subnums(int a, int b) {
    return a - b;
}

int multnums(int a, int b) {
    return a * b;
}

int divnums(int a, int b) {
    return a / b;
}

int andnums(int a, int b) {
    return a & b;
}

int ornums(int a, int b) {
    return a | b;
}

int xornums(int a, int b) {
    return a ^ b;
}

int lshiftnums(int a, int b) {
    return a << b;
}

unsigned int rshiftnums(unsigned int a, int b) {
    return a >> b;
}
